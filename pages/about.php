<h1>HM <em>Interface</em></h1>
<h2>Archéologie prospective des interfaces</h2>

<p class="large">
Ce site présente une interface d’exploration des origines et du devenir des IHM –Interfaces Homme-Machine - Son propos est d'offrir un outil d’archivage, d'organisation logique et de réflexion sur l'évolution des IHM (en anglais « HMI »). C’est aussi un outil de prospective. HMInterface propose de mieux comprendre le fondement de chaque interface pour tenter d’en déduire le futur… qui bien souvent semble à son tour nous rapprocher de ses origines premières. 
</p><p class="large">
Un exemple :
</p><p>
Les outils de pointage, comme la souris, tendent à s’effacer pour revenir à leur état premier, au plus près du geste, au contact direct du doigt (interfaces tactiles), au pointage du doigt à distance (capture de mouvement), pour évoluer enfin vers l’interprétation directe de l’intention de désignation (« BCI » ou  Interaction Cerveau-Ordinateur).
</p><p>
Quelles fonctions, quels organes, quels besoins ont déterminé nos interfaces "artificielles" actuelles ? Quelles mutations, quelles évolutions, quelles avancées ont accompagné leur élaboration et quelles sont les tendances de la recherche les plus pertinentes qui préfigurent leur devenir dans le respect des intentions originelles?
</p><p>
Si les machines sont des outils, les interfaces sont les instruments de médiation qui permettent de les contrôler. La finalité de ces assemblages Interface-Machine, ne peut se réduire à la notion d’innovation. L’innovation n’est recevable que lorsqu’elle permet amélioration de  la qualité de notre action sur le monde. La question est moins de savoir comment l’homme communique avec la machine que de comprendre comment la communication établie constitue une évolution positive de nos échanges avec le monde et avec les autres. 
</p><p>
Ces systèmes interfacés sont eux-mêmes des interfaces, des objets communicants destinés à améliorer notre relation au monde. Ils tendent, dans une perspective historique, à dépasser la notion de contrôle (interaction Homme-Monde), pour  favoriser la notion de dialogue : interaction d’Humain à Humain (H2H). Human to Human interaction.
</p><p><em>
Développé dans le cadre du cours « IHM et conception des objets communicants » par les étudiants du Master NET de l’université Paris 8, sous la direction de Maurice Benayoun, ce projet en construction est ouvert à toutes les contributions su sceptibles d’enrichir la base de données et les analyses qui lui donnent sens.</em>
</p>

<h2>Interfaces Homme-Machine.</h2>
<p>
Ce site - comme tout site - est une interface.
</p><p>
Pris dans une boucle récursive, son propos est d'offrir un outil de stockage, d'organisation logique et de réflexion sur l'évolution des IHM - Interfaces Homme Machine - d'un point de vue anthropocentré.
</p><p>
De quelles fonctions, de quels organes, de quels besoins découlent nos interfaces "artificielles" modernes ? Quelles mutations, quelles évolutions, quelles avancées ont amené l'Homme à construire nos outils modernes ?
</p><p>
Outils : voilà ce que sont ces machines. Outils, moyens : non des fins en soi. La Machine ne communique pas avec l'Homme. L'Homme communique avec son semblable à travers la Machine.
</p><p>
La Machine de nos IHM est ainsi elle-même une interface, un objet communiquant : elle permet de transmettre des messages aux êtres humains.
</p><p>
De ce point de vue, nos IHM - Interfaces Homme Machine - sont alors des IHH - Interfaces Homme - Homme - via la Machine. A l'instar de ce site&hellip;
</p>



